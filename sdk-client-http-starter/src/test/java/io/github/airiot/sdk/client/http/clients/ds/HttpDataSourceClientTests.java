package io.github.airiot.sdk.client.http.clients.ds;


import io.github.airiot.sdk.client.service.ds.DataServiceClient;
import org.junit.jupiter.api.MethodOrderer;
import org.junit.jupiter.api.Test;
import org.junit.jupiter.api.TestInstance;
import org.junit.jupiter.api.TestMethodOrder;
import org.junit.jupiter.api.extension.ExtendWith;
import org.mockito.junit.jupiter.MockitoExtension;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.boot.test.context.SpringBootTest;
import org.springframework.test.context.ActiveProfiles;

import java.util.Collections;

@SpringBootTest
@ExtendWith(MockitoExtension.class)
@ActiveProfiles("integration")
@TestInstance(TestInstance.Lifecycle.PER_CLASS)
@TestMethodOrder(MethodOrderer.OrderAnnotation.class)
public class HttpDataSourceClientTests {

    @Autowired
    private DataServiceClient dataServiceClient;
    
    @Test
    void testCall() {
        Student student = dataServiceClient.call(Student.class, "queryNode", Collections.singletonMap("id", "64e6f91e9fc90d249671a0f2")).unwrap();
        System.out.println(student);
    }

    static class Student {
        private String id;
        private String name;
        private Integer age;


        public String getId() {
            return id;
        }

        public String getName() {
            return name;
        }

        public Integer getAge() {
            return age;
        }

        @Override
        public String toString() {
            return "Student{" +
                    "id='" + id + '\'' +
                    ", name='" + name + '\'' +
                    ", age=" + age +
                    '}';
        }
    }
}
